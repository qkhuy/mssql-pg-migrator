var errNotImplemented = errors.New("postgres target: live connection not implemented yet")

// --- target.Mapper (pure, no connection) ---------------------------------

// MapIdentifier folds to lower case: PostgreSQL lower-cases unquoted
// identifiers, so this is the faithful, collision-free default.
func (t *Target) MapIdentifier(name string) string {
	return strings.ToLower(name)
}

// MapType maps a canonical type to a PostgreSQL native type. Deterministic and
// rule-based — no guessing. Unknown source types fall back to text and are
// flagged lossy so the report surfaces them for manual review.
func (t *Target) MapType(ct ir.CanonicalType) target.TypeMapping {
	switch ct.Kind {
	case ir.KindBool:
		return target.TypeMapping{Native: "boolean"}
	case ir.KindInt:
		switch {
		case ct.BitWidth <= 16:
			return target.TypeMapping{Native: "smallint"}
		case ct.BitWidth <= 32:
			return target.TypeMapping{Native: "integer"}
		default:
			return target.TypeMapping{Native: "bigint"}
		}
	case ir.KindFloat:
		if ct.BitWidth <= 32 {
			return target.TypeMapping{Native: "real"}
		}
		return target.TypeMapping{Native: "double precision"}
	case ir.KindDecimal:
		if ct.Precision > 0 {
			return target.TypeMapping{Native: fmt.Sprintf("numeric(%d,%d)", ct.Precision, ct.Scale)}
		}
		return target.TypeMapping{Native: "numeric"}
	case ir.KindString:
		if ct.Length <= 0 {
			return target.TypeMapping{Native: "text"}
		}
		if ct.Fixed {
			return target.TypeMapping{Native: fmt.Sprintf("char(%d)", ct.Length)}
		}
		return target.TypeMapping{Native: fmt.Sprintf("varchar(%d)", ct.Length)}
	case ir.KindText:
		return target.TypeMapping{Native: "text"}
	case ir.KindBinary:
		return target.TypeMapping{Native: "bytea"}
	case ir.KindDate:
		return target.TypeMapping{Native: "date"}
	case ir.KindTime:
		return target.TypeMapping{Native: "time"}
	case ir.KindTimestamp:
		return target.TypeMapping{Native: "timestamp"}
	case ir.KindTimestampTZ:
		return target.TypeMapping{Native: "timestamptz"}
	case ir.KindUUID:
		return target.TypeMapping{Native: "uuid"}
	case ir.KindJSON:
		return target.TypeMapping{Native: "jsonb"}
	default:
		native := ct.Native
		if native == "" {
			native = "unknown"
		}
		return target.TypeMapping{
			Native: "text",
			Lossy:  true,
			Note:   fmt.Sprintf("kiểu nguồn %q chưa có ánh xạ — review thủ công (có thể cần extension như PostGIS)", native),
		}
	}
}

// --- target.Target -------------------------------------------------------

// RenderDDL produces CREATE TABLE statements (columns + primary keys) and a
// warning per lossy/unsupported construct. Indexes and foreign keys are
// rendered separately by the pipeline after bulk load. Pure — no connection.
func (t *Target) RenderDDL(s *ir.Schema) ([]string, []target.Warning, error) {
	var stmts []string
	var warnings []target.Warning

	for _, tbl := range s.Tables {
		name := t.qualified(tbl.Schema, tbl.Name)
		var cols []string
		for _, c := range tbl.Columns {
			m := t.MapType(c.Type)
			if m.Lossy {
				warnings = append(warnings, target.Warning{
					Object:  fmt.Sprintf("%s.%s", name, t.MapIdentifier(c.Name)),
					Message: m.Note,
				})
			}
			line := fmt.Sprintf("  %s %s", t.MapIdentifier(c.Name), m.Native)
			if c.IsIdentity {
				line += " GENERATED BY DEFAULT AS IDENTITY"
			}
			if !c.Nullable {
				line += " NOT NULL"
			}
			cols = append(cols, line)
		}
		if pk := tbl.PrimaryKey; pk != nil && len(pk.Columns) > 0 {
			lowered := make([]string, len(pk.Columns))
			for i, c := range pk.Columns {
				lowered[i] = t.MapIdentifier(c)
			}
			cols = append(cols, fmt.Sprintf("  PRIMARY KEY (%s)", strings.Join(lowered, ", ")))
		}
		stmts = append(stmts, fmt.Sprintf("CREATE TABLE %s (\n%s\n);", name, strings.Join(cols, ",\n")))
	}

	for _, v := range s.Views {
		warnings = append(warnings, target.Warning{
			Object:  t.qualified(v.Schema, v.Name),
			Message: "view: định nghĩa SQL cần dịch sang PostgreSQL & review thủ công",
		})
	}
	for _, r := range s.Routines {
		warnings = append(warnings, target.Warning{
			Object:  t.qualified(r.Schema, r.Name),
			Message: r.Kind.String() + ": logic cần dịch sang PL/pgSQL & review thủ công",
		})
	}
	return stmts, warnings, nil
}

func (t *Target) qualified(schema, name string) string {
	if schema == "" {
		return t.MapIdentifier(name)
	}
	return t.MapIdentifier(schema) + "." + t.MapIdentifier(name)
}

